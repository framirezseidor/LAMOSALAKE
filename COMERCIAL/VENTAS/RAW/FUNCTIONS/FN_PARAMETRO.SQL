CREATE OR REPLACE FUNCTION RAW.FN_BUSCA_PARAMETRO(
    P_EXTRACTOR VARCHAR(100),
    P_NEGOCIO VARCHAR(50),
    P_NOMBRE VARCHAR(20)
)
RETURNS STRING
LANGUAGE SQL
AS
$$
    SELECT
        CONCAT(COALESCE(R_INICIO,''),'-', COALESCE(R_FIN,'')) VALOR
    FROM 
        RAW.PARAMETROS_EXTRACCION
    WHERE 
        EXTRACTOR = P_EXTRACTOR AND NEGOCIO = P_NEGOCIO AND PARAMETRO = P_NOMBRE
    LIMIT 1
$$;
