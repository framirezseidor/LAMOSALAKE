CREATE OR REPLACE FUNCTION RAW.FN_OBTENER_FECHA_INICIO("FECHA" DATE, "DIAS" INT)
RETURNS DATE
LANGUAGE SQL
AS
'/*LA FUNCION CALCULA LA FECHA DE INICIO TOMANDO EN CUENTA LOS DÍAS, SI EL NÚMERO DE DÍAS ES IGUAL O MENOR A DIAS
ENTONCES LA FECHA DE INICIO SERÁ EL 1 DEL MES ANTERIOR, SI ES MAYOR, ENTONCES DEL MES ACTUAL */

    SELECT DATE_TRUNC(month,DATEADD(DAY, -1 * DIAS, FECHA))  FECHA_INICIAL,
'
;
