    DEUDOR_ID,
    DEUDOR_TEXT,
    RAMO_ID,
    GRUPOSOLICITANTE_ID,
    PAIS_ID,
    REGION_ID,
    LOCALIDAD,
    RFC_ID,
    CLASIFICACIONCLIENTE_ID,
