CREATE OR alter VIEW  CON.VW_DIM_ORG_SOCIEDAD 
WITH ROW ACCESS POLICY SEGURIDAD.RAP_SAP_DIM_SOCIEDAD_ID ON (SOCIEDAD_ID)
AS
SELECT
S.SOCIEDAD_ID,
S.SOCIEDAD_TEXT,
S.PAIS_ID,
P.PAIS_TEXT,
S.NEGOCIOGRUPO_ID,
N.NEGOCIOGRUPO_TEXT,
S.SISORIGEN_ID,
S.MANDANTE,
S.FECHA_CARGA,
S.ZONA_HORARIA

FROM CON.DIM_ORG_SOCIEDAD S
LEFT JOIN CON.DIM_GEO_PAIS P ON S.PAIS_ID=P.PAIS_ID 
LEFT JOIN CON.DIM_ORG_NEGOCIOGRUPO N ON S.NEGOCIOGRUPO_ID=N.NEGOCIOGRUPO_ID 