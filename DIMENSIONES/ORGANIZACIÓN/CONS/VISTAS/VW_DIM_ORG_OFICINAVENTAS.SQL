CREATE OR alter VIEW  CON.VW_DIM_ORG_OFICINAVENTAS 
WITH ROW ACCESS POLICY SEGURIDAD.RAP_SAP_DIM_OFICINAVENTAS_ID ON (OFICINAVENTAS_ID) AS

WITH PASO1 AS (
SELECT
    OFICINAVENTAS_ID,
    OFICINAVENTAS_TEXT,
    SISORIGEN_ID,
    MANDANTE,
    FECHA_CARGA,
    ZONA_HORARIA
FROM CON.DIM_ORG_OFICINAVENTAS
), PASO2 AS (
SELECT
    NULL OFICINAVENTAS_ID,
    'SIN ASIGNAR' OFICINAVENTAS_TEXT,
    '--' SISORIGEN_ID,
    '--' MANDANTE,
    CURRENT_TIMESTAMP() FECHA_CARGA,
    '--' ZONA_HORARIA

), PASO3 AS (
SELECT
    '' OFICINAVENTAS_ID,
    'SIN ASIGNAR 2' OFICINAVENTAS_TEXT,
    '--' SISORIGEN_ID,
    '--' MANDANTE,
    CURRENT_TIMESTAMP() FECHA_CARGA,
    '--' ZONA_HORARIA

), FINAL AS (
    SELECT * FROM PASO1
    UNION ALL
    SELECT * FROM PASO2
    UNION ALL
    SELECT * FROM PASO3    
)

SELECT * FROM FINAL