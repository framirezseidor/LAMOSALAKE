CREATE OR alter VIEW  CON.VW_DIM_GEO_REGION AS

SELECT
R.REGION_ID,
R.PAIS_ID,
P.PAIS_TEXT,
R.REGION,
R.REGION_TEXT,
R.REGION_NEGADH_ID,
N.REGION_NEGADH_TEXT,
R.SISORIGEN_ID, 
R.MANDANTE, 
R.FECHA_CARGA, 
R.ZONA_HORARIA 
FROM CON.DIM_GEO_REGION R
LEFT JOIN CON.DIM_GEO_PAIS P ON R.PAIS_ID=P.PAIS_ID
LEFT JOIN CON.DIM_GEO_REGION_NEGADH N ON R.REGION_NEGADH_ID=N.REGION_NEGADH_ID
