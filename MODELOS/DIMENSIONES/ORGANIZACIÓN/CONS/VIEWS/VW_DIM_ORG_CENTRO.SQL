CREATE OR alter VIEW  CON.VW_DIM_ORG_CENTRO AS

SELECT
C.CENTRO_ID,
C.CENTRO_TEXT,
C.ORGVENTAS_ID,
O.ORGVENTAS_TEXT,
C.PAIS_ID,
P.PAIS_TEXT,
C.REGION_ID,
R.REGION_TEXT,
C.SISORIGEN_ID,
C.MANDANTE,
C.FECHA_CARGA,
C.ZONA_HORARIA

FROM CON.DIM_ORG_CENTRO C
LEFT JOIN CON.DIM_ORG_ORGANIZACIONVENTAS O ON C.ORGVENTAS_ID=O.ORGVENTAS_ID 
LEFT JOIN CON.DIM_GEO_PAIS P ON C.PAIS_ID=P.PAIS_ID 
LEFT JOIN CON.DIM_GEO_REGION R ON C.REGION_ID=R.REGION_ID 