CREATE OR REPLACE VIEW CON.VW_DIM_FIN_CENTROBENEFICIO AS
SELECT 
    DFCB.CENTROBENEFICIO_ID,
    DFCB.CENTROBENEFICIO,
    DFCB.CENTROBENEFICIO_TEXT,
    DFCB.SOCIEDADCO_ID,
    DFSC.SOCIEDADCO_TEXT,
    DFCB.SISORIGEN_ID,
    DFCB.MANDANTE,
    DFCB.FECHA_CARGA,
    DFCB.ZONA_HORARIA
FROM CON.DIM_FIN_CENTROBENEFICIO DFCB
LEFT JOIN CON.DIM_FIN_SOCIEDADCO DFSC
ON DFCB.SOCIEDADCO_ID = DFSC.SOCIEDADCO_ID

