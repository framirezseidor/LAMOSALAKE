CREATE OR REPLACE VIEW CON.VW_DIM_MAT_MATERIALCENTRO AS
SELECT
    MCENTRO.MATERIALCENTRO_ID,
    MCENTRO.MATERIALCENTRO_TEXT,
    MCENTRO.MATERIAL_ID,
    M.MATERIAL_TEXT,
    MCENTRO.CENTRO_ID,
    ORG_CENTRO.CENTRO_TEXT,--
    MCENTRO.PLANIFNECESIDADES_ID,
    PLANIF_NECESIDADES.PLANIFNECESIDADES_TEXT,
    MCENTRO.INDICADOR_ABC_ID,
    INDICADOR_ABC.INDICADOR_ABC_TEXT,
    MCENTRO.CATEGORIAVALORACION_ID,
    CATEGORIA_VAL.CATEGORIAVALORACION_TEXT,
    MCENTRO.STATUSMAT_ESPCENTRO_ID,
    STATUS_MAT.STATUSMATERIAL_TEXT AS STATUSMAT_ESPCENTRO_TEXT, --- EN MAPEO DICE STATUSMAT_ESPCENTRO_TEXT
    MCENTRO.SISORIGEN_ID,
    MCENTRO.FECHA_CARGA,
    MCENTRO.ZONA_HORARIA,
    MCENTRO.MANDANTE
FROM  CON.DIM_MAT_MATERIALCENTRO AS  MCENTRO
LEFT JOIN CON.DIM_MAT_MATERIAL AS M
    ON MCENTRO.MATERIAL_ID = M.MATERIAL_ID 
LEFT JOIN CON.DIM_ORG_CENTRO AS ORG_CENTRO 
    ON MCENTRO.CENTRO_ID = ORG_CENTRO.CENTRO_ID 
LEFT JOIN CON.DIM_MAT_PLANIFNECESIDADES AS PLANIF_NECESIDADES  
    ON MCENTRO.PLANIFNECESIDADES_ID = PLANIF_NECESIDADES.PLANIFNECESIDADES_ID
LEFT JOIN CON.DIM_MAT_INDICADOR_ABC AS INDICADOR_ABC 
    ON MCENTRO.INDICADOR_ABC_ID = INDICADOR_ABC.INDICADOR_ABC_ID
LEFT JOIN CON.DIM_MAT_CATEGORIAVALORACION AS CATEGORIA_VAL 
    ON MCENTRO.CATEGORIAVALORACION_ID = CATEGORIA_VAL.CATEGORIAVALORACION_ID
LEFT JOIN CON.DIM_MAT_STATUSMATERIAL AS STATUS_MAT 
    ON MCENTRO.STATUSMAT_ESPCENTRO_ID = STATUS_MAT.STATUSMATERIAL_ID