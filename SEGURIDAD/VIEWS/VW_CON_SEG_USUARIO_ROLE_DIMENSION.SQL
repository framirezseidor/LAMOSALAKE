create or replace view SEGURIDAD.VW_CON_SEG_USUARIO_ROLE_DIMENSION(
	USUARIO,
	ROLE_USUARIO,
	DIM_ROL,
	VALOR_ROL
) as
SELECT DISTINCT
    UR.USER_NM AS USUARIO,
    RD.ROLE_NM AS ROLE_USUARIO,
    RD.DIM AS DIM_ROL,
    RD.VALOR AS VALOR_ROL
FROM 
    SEGURIDAD.SEG_USUARIO_ROLE UR
INNER JOIN 
    SEGURIDAD.SEG_ROLE_DIMENSION RD
ON 
    UR.ROLE_NM = RD.ROLE_NM;